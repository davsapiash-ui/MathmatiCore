const fs = require('fs');
const files = [
  'מתמטיקאור - מסמך מאסטר - פיתוח.md',
  'מתמטיקאור- ארכיטקטורת המידע ומפת האתר - פיתוח.md',
  'מתמטיקאור- מסמך אפיון מפורט לקראת פיתוח - פיתוח.md',
  'מתמטיקאור- מסמך אפיון רצף פעילויות מעודכן - פיתוח.md'
];

const updateContent = `
### עדכונים אחרונים (07.07.2026)
* **מנעולי התנהגות פדגוגיים (Behavioral Locks):** שודרגה המערכת משלב של בדיקת תוצאה סופית למעקב התנהגותי. מעתה, מעבר בין משימות מותנה בביצוע פעולות פיזיות כמו הקפצה, פריטה ומחיקת בלוקים, כדי למנוע מעקפים ולוודא שהתלמיד הפנים את התהליך.
* **הרשאות ניהול בדשבורד מורה:** מנהלי מערכת יכולים כעת לצפות בנתוני כלל התלמידים וההתראות דרך מסך דשבורד מורה.
* **שמות כלים:** מחסן כלים שונה ל-ארגז כלים, כפתורי הלוח עודכנו להצג/הסתר לוח מוחשי לשיפור ההבנה הקוגניטיבית.
`;

files.forEach(f => {
  const p = 'מסמכי אפיון/' + f;
  let text = fs.readFileSync(p, 'utf8');
  text += updateContent;
  fs.writeFileSync(p, text);
});
console.log('Appended to Spec');
